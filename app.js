
const contantimg1 = document.querySelectorAll('.animation2')
window.addEventListener('scroll', checkBoxes2)
checkBoxes2()

function checkBoxes2() {
    const triggerBottom = window.innerHeight / 5 * 4
    contantimg1.forEach(animation2 => {
        const animationTop = animation2.getBoundingClientRect().top
        if (animationTop < triggerBottom) {
            animation2.classList.add('show')
        } else {
            animation2.classList.remove('show')
        }
    })
}

